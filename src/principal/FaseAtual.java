package principal;

/**
 * Enumeração que representa o estado atual da fase do jogo.
 * Utilizada para identificar em qual fase o jogador está:
 * FASE1, FASE2 ou FASE3.
 * 
 * Pode ser usada para lógica condicional, carregamento de fases
 * e controle de progresso no jogo.
 * 
 * @author Mateus
 * @version 1.0
 * @since 2025-06-13
 */
public enum FaseAtual {
	FASE1,
	FASE2,
	FASE3;
}
